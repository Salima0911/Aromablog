             <h2>Qu'est-ce que l'aromathérapie ?</h2>
             <p>Du grec aroma signifiant "arôme" et therapeia qui signifie "soins", l'aromathérapie désigne l'utilisation médicale d'extraits aromatiques de plantes, comprenant les essences et les huiles essentielles. C'est en 1935 que le mot a été inventé par le chimiste René-Maurice Gattefossé. Atteint d'une gangrène gazeuse, il applique en dernier recours de l'essence de lavande reconnue aujourd'hui pour ses propriétés cicatrisantes et antiseptiques. Surpris du résultat, il consacre alors sa vie à la recherche des propriétés des huiles essentielles. </p>
